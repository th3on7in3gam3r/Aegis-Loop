#!/usr/bin/env node
import { spawnSync } from 'node:child_process';
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const CODE_ROOT = join(__dirname, '..', '..');
const DEFAULT_API_URL = 'https://aegis-loop.com';

const args = process.argv.slice(2);
const command = args[0] ?? 'help';

function log(msg) {
  console.log(msg);
}

function fail(msg) {
  console.error(`\n✖ ${msg}\n`);
  process.exit(1);
}

function workflowTemplate(apiUrl) {
  return `name: Aegis Loop

on:
  pull_request:
    types: [opened, synchronize, reopened]

permissions:
  contents: read
  pull-requests: write

jobs:
  aegis:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - name: Run Aegis Loop PR scan
        env:
          AEGIS_API_KEY: \${{ secrets.AEGIS_API_KEY }}
          AEGIS_API_URL: ${apiUrl}
        run: |
          curl -sf -X POST "$AEGIS_API_URL/api/ci/scan" \\
            -H "Authorization: Bearer $AEGIS_API_KEY" \\
            -H "Content-Type: application/json" \\
            -d "{\\"repo\\":\\"\${{ github.repository }}\\",\\"pr\\":\${{ github.event.pull_request.number }}}"
`;
}

function configTemplate(apiUrl) {
  return `# Aegis Loop — generated by "npx aegis init"
apiUrl: ${apiUrl}

modules:
  - code
`;
}

function cmdInit() {
  const cwd = process.cwd();
  const apiUrl = process.env.AEGIS_API_URL || DEFAULT_API_URL;
  const force = args.includes('--force');

  const configDir = join(cwd, '.aegis');
  const configFile = join(configDir, 'config.yml');
  const workflowDir = join(cwd, '.github', 'workflows');
  const workflowFile = join(workflowDir, 'aegis-loop.yml');

  if (!force && existsSync(configFile)) {
    fail('`.aegis/config.yml` already exists — re-run with `--force` to overwrite.');
  }

  mkdirSync(configDir, { recursive: true });
  mkdirSync(workflowDir, { recursive: true });
  writeFileSync(configFile, configTemplate(apiUrl));
  writeFileSync(workflowFile, workflowTemplate(apiUrl));

  log('\n✓ Aegis Loop initialized\n');
  log(`  ${configFile}`);
  log(`  ${workflowFile}`);
  log('\nNext steps:');
  log('  1. Sign in at ' + apiUrl + '/login');
  log('  2. Settings → API keys → create a key');
  log('  3. Add GitHub repo secret AEGIS_API_KEY with that key');
  log('  4. Open a pull request — the workflow will scan automatically');
  log('\nLocal scan:  npx aegis scan [path]');
  log('');
}

function cmdScan() {
  const target = args[1] && !args[1].startsWith('-') ? args[1] : '.';
  const abs = resolve(process.cwd(), target);
  const scannerCli = join(CODE_ROOT, 'src', 'cli.ts');
  const result = spawnSync(
    process.execPath,
    ['--import', 'tsx', scannerCli, abs],
    { stdio: 'inherit', cwd: CODE_ROOT, env: process.env }
  );
  process.exit(result.status ?? 1);
}

function cmdHelp() {
  log(`
Aegis Loop CLI — find and fix vulnerabilities in your repos

Usage:
  npx aegis init [--force]     Scaffold .aegis/config.yml + GitHub Action
  npx aegis scan [path]        Scan a local directory (default: .)
  npx aegis help

Environment:
  AEGIS_API_URL   Hosted API (default: ${DEFAULT_API_URL})

Docs: ${DEFAULT_API_URL}/#pricing
`);
}

switch (command) {
  case 'init':
    cmdInit();
    break;
  case 'scan':
    cmdScan();
    break;
  case 'help':
  case '--help':
  case '-h':
    cmdHelp();
    break;
  default:
    fail(`Unknown command "${command}". Run "npx aegis help".`);
}
